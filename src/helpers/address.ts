export const isBasedOnIRIS = (city: string): boolean => {
  if (city === 'clichy') {
    return isBasedOnIRIS('clichy-la-garenne');
  }

  return (
    !city ||
    !citiesManaged.includes(
      city.replace(/ \d{1,2}e Arrondissement$/, '').toLowerCase()
    )
  );
};
